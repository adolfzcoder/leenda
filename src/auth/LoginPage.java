    private void btnLoginOnClick(java.awt.event.ActionEvent evt) throws FileNotFoundException, UnsupportedLookAndFeelException {//GEN-FIRST:event_btnLoginOnClick
        String emailRegex = "^[\\w-\\.]+@[\\w-]+\\.[a-z]{2,4}$";  // Basic email validation
        
        String role = cboRole.getItemAt(cboRole.getSelectedIndex());
        String email = txtEmail.getText().trim(); 
        char[] passwordChars = txtPassword.getPassword();// Get the password from the password field
        String password = new String(passwordChars).trim();

        //LoginService loginService = new LoginService();

        // Validate if email is not empty
        if (email.equals("")) {
            JOptionPane.showMessageDialog(this, "Email must be entered.", "Email missing", JOptionPane.ERROR_MESSAGE);
            // Validate if email matches the regex pattern
        } else if (!email.matches(emailRegex)) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address.", "Invalid Email", JOptionPane.ERROR_MESSAGE);
            // Validate if password is not empty
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(this, "Password must be entered.", "Password missing", JOptionPane.ERROR_MESSAGE);
            // Validate password against the regex pattern for letters, digits, and special characters
        }
        // Proceed to authenticate the user
        else {  
            AuthFunctions.login(role, email, password, this); 
        }
    }//GEN-LAST:event_btnLoginOnClick

    
    private void chkShowPasswordOnClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkShowPasswordOnClick
        // TODO add your handling code here:
        if(chkShowPassword.isSelected()){
            txtPassword.setEchoChar('\u0000');
            chkShowPassword.setText("Hide password");
        } else{
            txtPassword.setEchoChar('*');
            chkShowPassword.setText("Show password");
        }
    }//GEN-LAST:event_chkShowPasswordOnClick

